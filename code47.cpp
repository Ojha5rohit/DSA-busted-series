// switch case: --> Isme jo humara (int) hai wo (case) check krta hai ki (switch) mei present jo (expression) hai wo konse (case) se match krta hai. 
#include<iostream>
using namespace std;
int main(){

    int num = 2;

    cout<<endl;
    switch (num){

        case 1:cout<<"first"<<endl;
            break;

        case 2:cout<<"second"<<endl;
            break;

        default: cout<<"It is default"<<endl;   
          
    }
    cout<<endl;
    


}